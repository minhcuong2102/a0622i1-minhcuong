package bai_1.khai_bao_bien.src;

public class KhaiBaoBien {
    public static void main(String[] args) {
        int i = 0;
        char c = 'c';
        String s = "Codegym";
        float f = 20.5f;
        System.out.println("i = " + i);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
        System.out.println("f = " + f);
    }
}
